import type { AutostartReport, AutostartStatus } from "./autostart.js";
import type { RunResult } from "./systemd-installer.js";

/** Inputs for the PowerShell autostart script. */
export interface AutostartPs1Spec {
  readonly hummingCommand: string;
}

/** Subset of process.env needed to resolve the scheduled-task principal. */
export interface WindowsUserEnv {
  readonly USERDOMAIN?: string | undefined;
  readonly USERNAME?: string | undefined;
  readonly COMPUTERNAME?: string | undefined;
}

/**
 * Resolve the `<UserId>` for the scheduled task's principal (pure).
 *
 * schtasks resolves `<UserId>` to a SID via LookupAccountName, which needs the
 * NetBIOS domain/computer name — NOT the DNS hostname `os.hostname()` returns.
 * On a machine whose DNS name differs from its NetBIOS name (or is a FQDN),
 * `hostname\user` fails with "No mapping between account names and security IDs".
 * Windows exposes the authoritative values as `%USERDOMAIN%\%USERNAME%`, so we
 * prefer those; fall back to `%COMPUTERNAME%`, then the injected hostname.
 *
 * @throws {Error} when no username can be determined.
 */
export function resolveWindowsUserId(env: WindowsUserEnv, fallbackHostname: string): string {
  const username = env.USERNAME?.trim();
  if (!username) {
    throw new Error("cannot resolve Windows UserId: %USERNAME% is empty");
  }
  const domain = env.USERDOMAIN?.trim() || env.COMPUTERNAME?.trim() || fallbackHostname.trim();
  if (!domain) {
    throw new Error("cannot resolve Windows UserId: no USERDOMAIN/COMPUTERNAME/hostname");
  }
  return `${domain}\\${username}`;
}

/**
 * Render the `.ps1` body that starts the gateway (pure).
 *
 * Intentionally omits `--agent`: `gateway start` resolves the agent itself
 * from settings.json `runtime.agent`. Baking it in would freeze a stale value.
 */
export function renderAutostartPs1(spec: AutostartPs1Spec): string {
  return `# Autogenerated by humming autostart. Do not edit.\n${spec.hummingCommand} gateway start\n`;
}

/** Inputs for the boot-triggered scheduled task XML. */
export interface TaskXmlSpec {
  readonly description: string;
  readonly pwshPath: string;
  readonly ps1Path: string;
  readonly userId: string;
}

/** Render schtasks-compatible task XML (pure).
 *
 * Uses a LogonTrigger (not BootTrigger) so the task registers and runs entirely
 * as the current user without administrator rights — mirroring the Linux
 * systemd *user* service. A BootTrigger would require elevation to register and
 * would run before logon under a non-interactive session; the gateway only
 * needs to be up while the user is active, so logon-time start is sufficient
 * and keeps the whole install/enable/disable/uninstall lifecycle admin-free.
 */
export function renderTaskXml(spec: TaskXmlSpec): string {
  return `<?xml version="1.0" encoding="UTF-16"?>
<Task version="1.3" xmlns="http://schemas.microsoft.com/windows/2004/02/mit/task">
  <RegistrationInfo>
    <Description>${spec.description}</Description>
  </RegistrationInfo>
  <Principals>
    <Principal id="Author">
      <UserId>${spec.userId}</UserId>
      <LogonType>InteractiveToken</LogonType>
    </Principal>
  </Principals>
  <Settings>
    <MultipleInstancesPolicy>IgnoreNew</MultipleInstancesPolicy>
    <StartWhenAvailable>true</StartWhenAvailable>
    <ExecutionTimeLimit>PT0S</ExecutionTimeLimit>
  </Settings>
  <Triggers>
    <LogonTrigger>
      <Enabled>true</Enabled>
      <UserId>${spec.userId}</UserId>
    </LogonTrigger>
  </Triggers>
  <Actions Context="Author">
    <Exec>
      <Command>${spec.pwshPath}</Command>
      <Arguments>-NoProfile -WindowStyle Hidden -File "${spec.ps1Path}"</Arguments>
    </Exec>
  </Actions>
</Task>
`;
}

/** Injected side effects for the Windows installer. */
export interface WindowsDeps {
  readonly readFile: (path: string) => string | null;
  readonly writeFile: (path: string, content: string) => void;
  readonly mkdirp: (dir: string) => void;
  readonly rm: (path: string) => void;
  readonly taskExists: (taskName: string) => boolean;
  readonly run: (cmd: string, args: readonly string[]) => RunResult;
}

/** Everything needed to install the Windows autostart task. */
export interface WindowsInstallArgs {
  readonly ps1Path: string;
  readonly ps1Spec: AutostartPs1Spec;
  readonly taskName: string;
  readonly taskXml: string;
  readonly deps: WindowsDeps;
}

/**
 * Write the ps1 (when changed) and (re)register the boot task via schtasks.
 * @throws {Error} when schtasks exits non-zero.
 */
export function installWindowsAutostart(args: WindowsInstallArgs): AutostartReport {
  const desiredPs1 = renderAutostartPs1(args.ps1Spec);
  const currentPs1 = args.deps.readFile(args.ps1Path);
  const ps1Current = currentPs1 === desiredPs1;
  const taskPresent = args.deps.taskExists(args.taskName);
  if (ps1Current && taskPresent) {
    return { kind: "already-current", mechanism: "windows-task", path: args.taskName };
  }
  if (!ps1Current) {
    const dir = args.ps1Path.slice(0, args.ps1Path.lastIndexOf("\\"));
    args.deps.mkdirp(dir);
    args.deps.writeFile(args.ps1Path, desiredPs1);
  }
  if (taskPresent) {
    runOrThrow(args.deps, "schtasks.exe", ["/delete", "/tn", args.taskName, "/f"]);
  }
  const xmlPath = `${args.ps1Path}.task.xml`;
  args.deps.writeFile(xmlPath, args.taskXml);
  runOrThrow(args.deps, "schtasks.exe", ["/create", "/tn", args.taskName, "/xml", xmlPath, "/f"]);
  return { kind: "installed", mechanism: "windows-task", path: args.taskName };
}

function runOrThrow(deps: WindowsDeps, cmd: string, cmdArgs: readonly string[]): void {
  const result = deps.run(cmd, cmdArgs);
  if (result.status !== 0) {
    throw new Error(`${cmd} ${cmdArgs.join(" ")} failed: ${result.stderr || result.stdout}`);
  }
}

/** Everything needed to disable (but keep) the Windows autostart task. */
export interface WindowsDisableArgs {
  readonly taskName: string;
  readonly deps: WindowsDeps;
}

/**
 * Disable the boot task without deleting it (semantics A: reversible).
 * Idempotent: reports `already-disabled` when the task is absent.
 * @throws {Error} when `schtasks /change /disable` exits non-zero.
 */
export function disableWindowsAutostart(args: WindowsDisableArgs): AutostartReport {
  if (!args.deps.taskExists(args.taskName)) {
    return { kind: "already-disabled", mechanism: "windows-task", path: args.taskName };
  }
  runOrThrow(args.deps, "schtasks.exe", ["/change", "/tn", args.taskName, "/disable"]);
  return { kind: "disabled", mechanism: "windows-task", path: args.taskName };
}

/** Everything needed to enable an already-installed Windows task (no writes). */
export interface WindowsEnableArgs {
  readonly ps1Path: string;
  readonly taskName: string;
  readonly deps: WindowsDeps;
}

/**
 * Enable an already-registered boot task without writing files. Requires the
 * task to exist (install first). Idempotent: reports `already-current` when the
 * task is already enabled.
 * @throws {Error} when `schtasks /change /enable` exits non-zero.
 */
export function enableWindowsAutostart(args: WindowsEnableArgs): AutostartReport {
  if (!args.deps.taskExists(args.taskName)) {
    return { kind: "not-installed", mechanism: "windows-task", path: args.taskName };
  }
  if (isWindowsTaskEnabled(args.deps, args.taskName)) {
    return { kind: "already-current", mechanism: "windows-task", path: args.taskName };
  }
  runOrThrow(args.deps, "schtasks.exe", ["/change", "/tn", args.taskName, "/enable"]);
  return { kind: "enabled", mechanism: "windows-task", path: args.taskName };
}

/** Everything needed to uninstall (delete task + files) the Windows autostart. */
export interface WindowsUninstallArgs {
  readonly ps1Path: string;
  readonly taskName: string;
  readonly deps: WindowsDeps;
}

/**
 * Delete the boot task and remove its ps1/xml files. Idempotent: reports
 * `already-uninstalled` when neither the task nor the ps1 file is present.
 * @throws {Error} when `schtasks /delete` exits non-zero.
 */
export function uninstallWindowsAutostart(args: WindowsUninstallArgs): AutostartReport {
  const taskPresent = args.deps.taskExists(args.taskName);
  const ps1Present = args.deps.readFile(args.ps1Path) !== null;
  if (!taskPresent && !ps1Present) {
    return { kind: "already-uninstalled", mechanism: "windows-task", path: args.taskName };
  }
  if (taskPresent) {
    runOrThrow(args.deps, "schtasks.exe", ["/delete", "/tn", args.taskName, "/f"]);
  }
  if (ps1Present) {
    args.deps.rm(args.ps1Path);
    args.deps.rm(`${args.ps1Path}.task.xml`);
  }
  return { kind: "uninstalled", mechanism: "windows-task", path: args.taskName };
}

/** Everything needed to query the Windows autostart task's status. */
export interface WindowsQueryArgs {
  readonly taskName: string;
  readonly deps: WindowsDeps;
}

/**
 * Whether a registered task's "Scheduled Task State:" line reads enabled.
 * Read-only; treats an unreadable/absent state line as disabled.
 */
function isWindowsTaskEnabled(deps: WindowsDeps, taskName: string): boolean {
  const out = deps.run("schtasks.exe", ["/query", "/tn", taskName, "/fo", "LIST", "/v"]);
  const stateLine = out.stdout
    .split(/\r?\n/)
    .map((line) => line.trim())
    .find((line) => line.startsWith("Scheduled Task State:"));
  return stateLine?.toLowerCase().includes("enabled") ?? false;
}

/**
 * Report whether the boot task exists and is enabled. Read-only. Parses the
 * "Scheduled Task State:" line from `schtasks /query /fo LIST /v`; treats an
 * unreadable/absent state line as disabled rather than throwing.
 */
export function queryWindowsAutostart(args: WindowsQueryArgs): AutostartStatus {
  if (!args.deps.taskExists(args.taskName)) {
    return { kind: "not-installed", mechanism: "windows-task", path: args.taskName };
  }
  if (isWindowsTaskEnabled(args.deps, args.taskName)) {
    return { kind: "enabled", mechanism: "windows-task", path: args.taskName };
  }
  return { kind: "installed-disabled", mechanism: "windows-task", path: args.taskName };
}
